void HexDumpLine(const char* label, const unsigned char* data, unsigned int cb)
{
    unsigned int i;
    printf("%s:", label);
    for (i = 0; i < cb; ++i) {
        printf("%02X", data[i]);
    }
    printf("\n");
}

int TestSha(void)
{
    static const unsigned char kMsg[] = "XboxNeighborhood rsa_compare";
    A_SHA_CTX ctx;
    unsigned char digest[A_SHA_DIGEST_LEN];

    A_SHAInit(&ctx);
    A_SHAUpdate(&ctx, (unsigned char*)kMsg, (unsigned int)(sizeof(kMsg) - 1));
    A_SHAFinal(&ctx, digest);
    HexDumpLine("sha_message", digest, sizeof(digest));

    {
        A_SHA_CTX ctx2;
        unsigned char digest2[A_SHA_DIGEST_LEN];
        DWORD len = (DWORD)(sizeof(kMsg) - 1);

        A_SHAInit(&ctx2);
        A_SHAUpdate(&ctx2, (unsigned char*)&len, sizeof(len));
        A_SHAUpdate(&ctx2, (unsigned char*)kMsg, len);
        A_SHAFinal(&ctx2, digest2);
        HexDumpLine("sha_xccalc_style", digest2, sizeof(digest2));
    }

    return 0;
}

