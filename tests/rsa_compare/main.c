int TestBsafeRoundtrip(void)
{
    LPBSAFE_PUB_KEY pub = (LPBSAFE_PUB_KEY)(void*)g_ImgbPublicKeyData;
    LPBSAFE_PRV_KEY prv = (LPBSAFE_PRV_KEY)(void*)g_ImgbPrivateKeyData;
    unsigned char plain[264];
    unsigned char enc[264];
    unsigned char dec[264];
    unsigned int i;

    memset(plain, 0, sizeof(plain));
    plain[254] = 0x42;

    if (!BSafeEncPublic(pub, plain, enc)) {
        printf("bsafe_enc:FAIL\n");
        return 1;
    }
    HexDumpLine("bsafe_enc", enc, (pub->bitlen + 7) / 8);

    if (!BSafeDecPrivate(prv, enc, dec)) {
        printf("bsafe_dec:FAIL\n");
        return 1;
    }
    HexDumpLine("bsafe_dec", dec, (prv->bitlen + 7) / 8);

    for (i = 0; i < sizeof(plain); ++i) {
        if (plain[i] != dec[i]) {
            printf("bsafe_roundtrip:FAIL\n");
            return 1;
        }
    }
    printf("bsafe_roundtrip:OK\n");
    return 0;
}

int TestXcSign(void)
{
    static const unsigned char kMsg[] = "image header digest sample";
    unsigned char digest[A_SHA_DIGEST_LEN];
    unsigned char sig[512];
    ULONG sigLen;
    A_SHA_CTX ctx;

    A_SHAInit(&ctx);
    A_SHAUpdate(&ctx, (unsigned char*)kMsg, (unsigned int)(sizeof(kMsg) - 1));
    A_SHAFinal(&ctx, digest);

    sigLen = XCCalcSigSize((PUCHAR)g_ImgbPrivateKeyData);
    if (!XCSignDigest((PUCHAR)digest,
            (PUCHAR)g_ImgbPrivateKeyData,
            sig)) {
        printf("xc_sign_digest:FAIL\n");
        return 1;
    }

    HexDumpLine("xc_sign_digest", sig, sigLen);
    return 0;
}
